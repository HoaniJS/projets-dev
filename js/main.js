let photos = document.querySelectorAll(".photos li");

/*************************************************************************************************/
/* ***************************************** FONCTIONS ***************************************** */
/*************************************************************************************************/



/*************************************************************************************************/
/* ************************************** CODE PRINCIPAL *************************************** */
/*************************************************************************************************/

for (let index = 0; index < photos.length; index++) {
    photos[index].addEventListener('click', function(){
        this.classList.toggle("selected");
        total.innerHTML = document.querySelectorAll(".selected").length;
    });

    photos[index].addEventListener('mouseover', function(){
        this.classList.add("overed");
    });
    photos[index].addEventListener('mouseleave', function(){
        this.classList.remove("overed");
    });
};
